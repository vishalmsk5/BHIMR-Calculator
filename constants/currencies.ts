export const searchCurrencies = (query: string) => {
  const lowerQuery = query.toLowerCase();
  return worldCurrencies.filter(
    c =>
      c.code.toLowerCase().includes(lowerQuery) ||
      c.name.toLowerCase().includes(lowerQuery)
  );
};
