export function t(key: TranslationKey, lang: string): string {
  const langTranslations = translations[lang] || translations['en'];
  return langTranslations[key] || translations['en'][key] || key;
}

export function toLocalNumber(numStr: string, lang: string): string {
  const digits = numberSystems[lang] || numberSystems['en'];
  if (!digits) return numStr;
  return numStr.replace(/[0-9]/g, (d) => digits[parseInt(d)] || d);
}

export function fromLocalNumber(localStr: string, lang: string): string {
  const digits = numberSystems[lang] || numberSystems['en'];
  if (!digits) return localStr;
  let result = localStr;
  for (let i = 0; i < 10; i++) {
    if (digits[i] !== String(i)) {
      result = result.split(digits[i]).join(String(i));
    }
  }
  return result;
}

export function usesLocalNumbers(lang: string): boolean {
  const digits = numberSystems[lang];
  return !!digits && digits[0] !== '0';
}